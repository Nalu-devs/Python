# Operadores matemáticos
a = 10
b = 3

print(a + b)  # soma
print(a - b)  # subtração
print(a * b)  # multiplicação
print(a / b)  # divisão real
print(a // b) # divisão inteira
print(a % b)  # resto da divisão
print(a ** b) # potência

# Operadores lógicos
x = True
y = False

print(x and y)  # False
print(x or y)   # True
print(not x)    # False

# Operadores relacionais
print(a > b)
print(a == 10)
print(b != 5)